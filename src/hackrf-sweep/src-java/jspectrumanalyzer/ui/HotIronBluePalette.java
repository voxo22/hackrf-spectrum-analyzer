	{
		return colors[i];
	}

	@Override public Color getColorNormalized(double value)
	{
		int index = (int) (colors.length * value);
		if (index < 0)
			index = 0;
		if (index >= colors.length)
			index = colors.length - 1;
		return colors[index];
	}

	@Override public int size()
	{
		return colors.length;
	}
